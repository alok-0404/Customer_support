/**
 * Ticket Model
 */

import mongoose from 'mongoose';

const ticketSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: [true, 'First name is required'],
    trim: true
  },
  lastName: {
    type: String,
    trim: true
  },
  email: {
    type: String,
    required: [true, 'Email is required'],
    trim: true,
    lowercase: true
  },
  phone: {
    type: String,
    trim: true
  },
  message: {
    type: String,
    required: [true, 'Message is required'],
    trim: true
  },
  status: {
    type: String,
    enum: ['received', 'open', 'closed'],
    default: 'received'
  }
}, {
  timestamps: true
});

export default mongoose.model('Ticket', ticketSchema);
